    private int currentPage = 0;
    
    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        if(currentPage == 0)
        {
            ((java.awt.CardLayout)jPanel1.getLayout()).next(jPanel1);
            backButton.setVisible(true);
            currentPage++;
        } else if(currentPage == 1) {
            ((java.awt.CardLayout)jPanel1.getLayout()).next(jPanel1);
            nextButton.setText("Done");
            currentPage++;
        } else if(currentPage == 2){
            //process done
            save();
            java.awt.event.WindowEvent wev = new java.awt.event.WindowEvent(this, java.awt.event.WindowEvent.WINDOW_CLOSING);
            java.awt.Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(wev);

            this.dispose();
            //this.dispose();
            currentPage++;
        }
    }//GEN-LAST:event_nextButtonActionPerformed

