    private void allowIncomingPacketsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allowIncomingPacketsActionPerformed
       javax.swing.JCheckBox checkbox = (javax.swing.JCheckBox)evt.getSource();
        if(checkbox.isSelected()){
            clientUPnPCheckBox.setEnabled(true);
            clientPortLabel.setEnabled(true);
            clientPortTextField.setEnabled(true);
            System.out.println("Selected");
        } else {
            clientUPnPCheckBox.setEnabled(false);
            clientPortLabel.setEnabled(false);
            clientPortTextField.setEnabled(false);
            System.out.println("Not Selected");
        }
    }//GEN-LAST:event_allowIncomingPacketsActionPerformed

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        if(currentPage <= 2){
            ((java.awt.CardLayout)jPanel1.getLayout()).previous(jPanel1);
            currentPage--;
            backButton.setVisible(false);
            nextButton.setText("Next");
        }
    }//GEN-LAST:event_backButtonActionPerformed

